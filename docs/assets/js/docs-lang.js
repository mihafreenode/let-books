const MOBILE_NAV_MEDIA_QUERY = '(max-width: 820px)';

function isDocsSite() {
  if (document.body.dataset.siteScope) {
    return document.body.dataset.siteScope === 'docs';
  }

  return /\/docs\//.test(window.location.pathname);
}

function createHeadLink(attributes) {
  const link = document.createElement('link');
  for (const [name, value] of Object.entries(attributes)) {
    link.setAttribute(name, value);
  }
  return link;
}

function getDocsFaviconBasePath() {
  const pageType = getCurrentPageType();
  return pageType === 'language-hub' ? '../public/favicon/' : '../../public/favicon/';
}

function ensureDocsFavicons() {
  const head = document.head;
  if (!head || head.querySelector('[data-docs-favicon]') || head.querySelector('link[rel="icon"]')) return;

  const basePath = getDocsFaviconBasePath();
  const entries = [
    { rel: 'icon', href: `${basePath}favicon.svg`, type: 'image/svg+xml' },
    { rel: 'icon', href: `${basePath}favicon-dark.svg`, type: 'image/svg+xml', media: '(prefers-color-scheme: dark)' },
    { rel: 'icon', href: `${basePath}favicon.ico`, sizes: 'any' },
    { rel: 'icon', href: `${basePath}favicon-32x32.png`, type: 'image/png', sizes: '32x32' },
    { rel: 'icon', href: `${basePath}favicon-16x16.png`, type: 'image/png', sizes: '16x16' },
    { rel: 'apple-touch-icon', href: `${basePath}apple-touch-icon.png`, sizes: '180x180' },
    { rel: 'manifest', href: `${basePath}site.webmanifest` },
  ];

  for (const entry of entries) {
    const link = createHeadLink(entry);
    link.dataset.docsFavicon = 'true';
    head.appendChild(link);
  }

  if (!head.querySelector('meta[name="theme-color"]')) {
    const meta = document.createElement('meta');
    meta.name = 'theme-color';
    meta.content = '#0f5b45';
    meta.dataset.docsFavicon = 'true';
    head.appendChild(meta);
  }
}

function inferLocaleFromPath() {
  const match = window.location.pathname.match(/\/docs\/([^/]+)\//);
  if (!match) return 'en';
  const segment = match[1];
  return DOCS_LOCALE_CONFIG.labels[segment] ? segment : 'en';
}

function inferPageTypeFromPath() {
  if (/\/docs\/?(?:index\.html)?$/.test(window.location.pathname)) return 'language-hub';
  if (/\/index\.html$/.test(window.location.pathname)) return 'overview';
  if (/\/(individuals|posamezniki)\.html$/.test(window.location.pathname)) return 'individuals';
  if (/\/(institutions|institucije)\.html$/.test(window.location.pathname)) return 'institutions';
  if (/\/(administrators|skrbniki)\.html$/.test(window.location.pathname)) return 'administrators';
  return 'overview';
}

function getCurrentLocale() {
  return document.body.dataset.locale || inferLocaleFromPath();
}

function getCurrentPageType() {
  return document.body.dataset.pageType || inferPageTypeFromPath();
}

function getLocaleShortLabel(locale) {
  return DOCS_LOCALE_CONFIG.localeShortLabels[locale] || locale;
}

function getDocsNavLabels(locale) {
  return DOCS_LOCALE_CONFIG.docsNavLabels[locale] || DOCS_LOCALE_CONFIG.docsNavLabels.en;
}

function getEquivalentPagePath(locale) {
  const pageType = getCurrentPageType();
  return DOCS_LOCALE_CONFIG.pageMap[pageType]?.[locale] || null;
}

function getEquivalentPageHref(locale) {
  const path = getEquivalentPagePath(locale);
  return path ? `../${path}` : '../index.html';
}

function createNavLink(href, text, { current = false } = {}) {
  const link = document.createElement('a');
  link.className = current ? 'nav-link is-current' : 'nav-link';
  link.href = href;
  link.textContent = text;
  if (current) {
    link.setAttribute('aria-current', 'page');
  }
  return link;
}

function createLanguageLink(locale, href, { current = false, fullLabel = false } = {}) {
  const link = document.createElement('a');
  link.className = current ? 'lang-link is-current' : 'lang-link';
  link.href = href;
  link.lang = locale;
  link.textContent = fullLabel ? (DOCS_LOCALE_CONFIG.labels[locale] || locale) : getLocaleShortLabel(locale);
  if (current) {
    link.setAttribute('aria-current', 'page');
  }
  return link;
}

function createLanguageSelector(locale, labels) {
  const menu = document.createElement('div');
  menu.className = 'nav-language-menu';

  const label = document.createElement('span');
  label.className = 'nav-language-label';
  label.textContent = labels.language;

  const select = document.createElement('select');
  select.className = 'nav-language-select';
  select.setAttribute('aria-label', labels.currentLanguage);

  const map = DOCS_LOCALE_CONFIG.pageMap[getCurrentPageType()];
  if (map) {
    for (const targetLocale of Object.keys(map)) {
      const option = document.createElement('option');
      option.value = getEquivalentPageHref(targetLocale);
      option.textContent = DOCS_LOCALE_CONFIG.labels[targetLocale] || targetLocale;
      option.selected = targetLocale === locale;
      select.appendChild(option);
    }
  }

  select.addEventListener('change', () => {
    if (select.value) {
      window.location.href = select.value;
    }
  });

  menu.append(label, select);
  return menu;
}

function upgradeDocsNavigation() {
  const pageType = getCurrentPageType();
  if (pageType === 'language-hub') return;

  const locale = getCurrentLocale();
  const labels = getDocsNavLabels(locale);
  const nav = document.querySelector('.site-header .header-nav');
  if (!nav) return;

  nav.replaceChildren(
    createNavLink('../index.html', labels.docsHome),
    createNavLink('../../index.html', labels.publicHomepage),
    createNavLink('https://github.com/mihafreenode/let-books', labels.github),
  );

  nav.setAttribute('aria-label', 'Documentation');
  nav.appendChild(createLanguageSelector(locale, labels));
}

function buildEquivalentLanguageLinks() {
  let container = document.querySelector('[data-equivalent-language-list]');

  const pageType = getCurrentPageType();
  const currentLocale = getCurrentLocale();
  const map = DOCS_LOCALE_CONFIG.pageMap[pageType];

  if (!map || !currentLocale) return;

  if (!container) {
    const footerLinks = document.querySelector('.footer-links');
    if (!footerLinks || !footerLinks.parentElement) return;
    container = document.createElement('div');
    container.className = 'language-list';
    container.dataset.equivalentLanguageList = 'true';
    container.style.marginTop = '0.85rem';
    footerLinks.parentElement.appendChild(container);
  }

  container.classList.add('equivalent-language-list');

  const fragment = document.createDocumentFragment();
  for (const [locale, path] of Object.entries(map)) {
    const link = document.createElement('a');
    link.href = path ? `../${path}` : '../index.html';
    link.textContent = DOCS_LOCALE_CONFIG.labels[locale] || locale;
    link.className = 'footer-language-link';
    if (locale === currentLocale) {
      link.setAttribute('aria-current', 'page');
      link.classList.add('is-current');
    }
    fragment.appendChild(link);
  }

  container.replaceChildren(fragment);
}

function ensureFooterMicrocopy() {
  const pageType = getCurrentPageType();
  if (pageType === 'language-hub') return;

  const locale = getCurrentLocale();
  const footerHeading = document.querySelector('.site-footer .footer-inner > div:first-child');
  if (!footerHeading) return;

   let paragraph = footerHeading.querySelector('.footer-microcopy');
   if (!paragraph) {
     paragraph = document.createElement('p');
     paragraph.className = 'footer-microcopy';
     footerHeading.appendChild(paragraph);
   }

     paragraph.textContent = DOCS_LOCALE_CONFIG.footerMicrocopy[locale] || DOCS_LOCALE_CONFIG.footerMicrocopy.en;
}

function normalizeDocsFooter() {
  const pageType = getCurrentPageType();
  if (pageType === 'language-hub') return;

  const locale = getCurrentLocale();
  const labels = getDocsNavLabels(locale);
  const footerLinks = document.querySelector('.site-footer .footer-links');
  if (!footerLinks) return;

  footerLinks.replaceChildren(
    createNavLink('../index.html', labels.docsHome),
    createNavLink('../../index.html', labels.publicHomepage),
    createNavLink('https://github.com/mihafreenode/let-books', labels.github),
  );
}

function getNavToggleLabels(locale) {
  return DOCS_LOCALE_CONFIG.navToggleLabels[locale] || DOCS_LOCALE_CONFIG.navToggleLabels.en;
}

function setupMobileNav() {
  const headerInner = document.querySelector('.site-header .header-inner');
  const nav = headerInner?.querySelector('.header-nav');

  if (!headerInner || !nav) return;

  document.body.classList.add('js-nav-ready');

  const locale = getCurrentLocale();
  const labels = getNavToggleLabels(locale);
  const navId = nav.id || `docs-nav-${getCurrentPageType()}`;
  nav.id = navId;

  let topRow = headerInner.querySelector('.header-top-row');
  if (!topRow) {
    topRow = document.createElement('div');
    topRow.className = 'header-top-row';

    while (headerInner.firstChild) {
      topRow.appendChild(headerInner.firstChild);
    }

    headerInner.appendChild(topRow);
    headerInner.appendChild(nav);
  }

  let toggle = headerInner.querySelector('.nav-toggle');
  if (!toggle) {
    toggle = document.createElement('button');
    toggle.type = 'button';
    toggle.className = 'nav-toggle';
    toggle.setAttribute('aria-controls', navId);
    toggle.innerHTML = '<span aria-hidden="true">☰</span>';
    topRow.appendChild(toggle);
  }

  const mobileMedia = window.matchMedia(MOBILE_NAV_MEDIA_QUERY);

  function setExpanded(expanded) {
    toggle.setAttribute('aria-expanded', expanded ? 'true' : 'false');
    toggle.setAttribute('aria-label', expanded ? labels.close : labels.open);
    nav.classList.toggle('is-open', expanded);

    if (mobileMedia.matches) {
      nav.hidden = !expanded;
    } else {
      nav.hidden = false;
    }
  }

  function closeMenu({ returnFocus = false } = {}) {
    setExpanded(false);
    if (returnFocus) {
      toggle.focus();
    }
  }

  function syncWithViewport() {
    if (mobileMedia.matches) {
      const isExpanded = toggle.getAttribute('aria-expanded') === 'true';
      nav.hidden = !isExpanded;
    } else {
      nav.classList.remove('is-open');
      nav.hidden = false;
      toggle.setAttribute('aria-expanded', 'false');
      toggle.setAttribute('aria-label', labels.open);
    }
  }

  toggle.addEventListener('click', () => {
    const nextExpanded = toggle.getAttribute('aria-expanded') !== 'true';
    setExpanded(nextExpanded);
  });

  toggle.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
      event.preventDefault();
      closeMenu({ returnFocus: true });
    }
  });

  nav.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
      event.preventDefault();
      closeMenu({ returnFocus: true });
    }
  });

  nav.addEventListener('click', (event) => {
    if (!mobileMedia.matches) return;
    if (event.target.closest('a')) {
      closeMenu();
    }
  });

  if (typeof mobileMedia.addEventListener === 'function') {
    mobileMedia.addEventListener('change', syncWithViewport);
  } else {
    mobileMedia.addListener(syncWithViewport);
  }

  setExpanded(false);
  syncWithViewport();
}

document.addEventListener('DOMContentLoaded', () => {
    const locale = getCurrentLocale();
    const pageType = getCurrentPageType();
    const docsSite = isDocsSite();

    document.body.dataset.locale = locale;
    document.body.dataset.pageType = pageType;
    if (docsSite) {
      ensureDocsFavicons();
      upgradeDocsNavigation();
    }
    setupMobileNav();
    if (docsSite) {
      buildEquivalentLanguageLinks();
      normalizeDocsFooter();
      ensureFooterMicrocopy();
    }
});
