function setupMobileNav() {
  const headerInner = document.querySelector('.site-header .header-inner');
  const nav = headerInner?.querySelector('.header-nav');

  if (!headerInner || !nav) return;

  document.body.classList.add('js-nav-ready');

  const locale = getCurrentLocale();
  const labels = getNavToggleLabels(locale);
  const navId = nav.id || `docs-nav-${getCurrentPageType()}`;
  nav.id = navId;

  let topRow = headerInner.querySelector('.header-top-row');
  if (!topRow) {
    topRow = document.createElement('div');
    topRow.className = 'header-top-row';

    while (headerInner.firstChild) {
      topRow.appendChild(headerInner.firstChild);
    }

    headerInner.appendChild(topRow);
    headerInner.appendChild(nav);
  }

  let toggle = headerInner.querySelector('.nav-toggle');
  if (!toggle) {
    toggle = document.createElement('button');
    toggle.type = 'button';
    toggle.className = 'nav-toggle';
    toggle.setAttribute('aria-controls', navId);
    toggle.innerHTML = '<span aria-hidden="true">☰</span>';
    topRow.appendChild(toggle);
  }

  const mobileMedia = window.matchMedia(MOBILE_NAV_MEDIA_QUERY);

  function setExpanded(expanded) {
    toggle.setAttribute('aria-expanded', expanded ? 'true' : 'false');
    toggle.setAttribute('aria-label', expanded ? labels.close : labels.open);
    nav.classList.toggle('is-open', expanded);

    if (mobileMedia.matches) {
      nav.hidden = !expanded;
    } else {
      nav.hidden = false;
    }
  }

  function closeMenu({ returnFocus = false } = {}) {
    setExpanded(false);
    if (returnFocus) {
      toggle.focus();
    }
  }

  function syncWithViewport() {
    if (mobileMedia.matches) {
      const isExpanded = toggle.getAttribute('aria-expanded') === 'true';
      nav.hidden = !isExpanded;
    } else {
      nav.classList.remove('is-open');
      nav.hidden = false;
      toggle.setAttribute('aria-expanded', 'false');
      toggle.setAttribute('aria-label', labels.open);
    }
  }

  toggle.addEventListener('click', () => {
    const nextExpanded = toggle.getAttribute('aria-expanded') !== 'true';
    setExpanded(nextExpanded);
  });

  toggle.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
      event.preventDefault();
      closeMenu({ returnFocus: true });
    }
  });

  nav.addEventListener('keydown', (event) => {
    if (event.key === 'Escape') {
      event.preventDefault();
      closeMenu({ returnFocus: true });
    }
  });

  nav.addEventListener('click', (event) => {
    if (!mobileMedia.matches) return;
    if (event.target.closest('a')) {
      closeMenu();
    }
  });

  if (typeof mobileMedia.addEventListener === 'function') {
    mobileMedia.addEventListener('change', syncWithViewport);
  } else {
    mobileMedia.addListener(syncWithViewport);
  }

  setExpanded(false);
  syncWithViewport();
}

function setupTopicMenus() {
  const groups = document.querySelectorAll('[data-topic-menu-group]');
  const currentUrl = new URL(window.location.href);
  const requestedTopic = currentUrl.searchParams.get('topic');

  function closeGroup(group) {
    const triggers = group.querySelectorAll('[data-topic-trigger]');
    const menus = group.querySelectorAll('[data-topic-menu]');

    for (const trigger of triggers) {
      trigger.setAttribute('aria-expanded', 'false');
      trigger.classList.remove('is-active');
    }

    for (const menu of menus) {
      menu.hidden = true;
    }
  }

  function openTopic(group, slug) {
    const trigger = group.querySelector(`[data-topic-slug="${CSS.escape(slug)}"]`);
    if (!trigger) return false;
    const key = trigger.getAttribute('data-topic-trigger');
    if (!key) return false;
    const menu = group.querySelector(`[data-topic-menu="${key}"]`);
    if (!menu) return false;
    closeGroup(group);
    trigger.setAttribute('aria-expanded', 'true');
    trigger.classList.add('is-active');
    menu.hidden = false;
    return true;
  }

  function syncTopicQuery(slug) {
    const url = new URL(window.location.href);
    if (slug) {
      url.searchParams.set('topic', slug);
    } else {
      url.searchParams.delete('topic');
    }
    window.history.replaceState({}, '', url);
  }

  for (const group of groups) {
    const triggers = Array.from(group.querySelectorAll('[data-topic-trigger]'));

    for (const trigger of triggers) {
      trigger.addEventListener('click', (event) => {
        event.stopPropagation();
        const key = trigger.getAttribute('data-topic-trigger');
        if (!key) return;

        const menu = group.querySelector(`[data-topic-menu="${key}"]`);
        if (!menu) return;
        const slug = trigger.getAttribute('data-topic-slug');

        const isOpen = trigger.getAttribute('aria-expanded') === 'true';
        closeGroup(group);
        syncTopicQuery(null);

        if (!isOpen) {
          trigger.setAttribute('aria-expanded', 'true');
          trigger.classList.add('is-active');
          menu.hidden = false;
          if (slug) {
            syncTopicQuery(slug);
          }
        }
      });
    }

    if (requestedTopic) {
      openTopic(group, requestedTopic);
    }
  }

  document.addEventListener('click', (event) => {
    for (const group of groups) {
      if (!group.contains(event.target)) {
        closeGroup(group);
        syncTopicQuery(null);
      }
    }
  });

  document.addEventListener('keydown', (event) => {
    if (event.key !== 'Escape') return;
    for (const group of groups) {
      closeGroup(group);
    }
    syncTopicQuery(null);
  });
}

function preserveTopicContextForSwitcher() {
  const currentUrl = new URL(window.location.href);
  const currentTopic = currentUrl.searchParams.get('topic');
  if (!currentTopic) return;

  const links = document.querySelectorAll('[data-content-switcher-link]');
  for (const link of links) {
    link.addEventListener('click', () => {
      const href = link.getAttribute('href');
      if (!href) return;
      const target = new URL(href, window.location.href);
      target.searchParams.set('topic', currentTopic);
      link.setAttribute('href', `${target.pathname.replace(window.location.origin, '')}${target.search}`);
    });
  }
}

